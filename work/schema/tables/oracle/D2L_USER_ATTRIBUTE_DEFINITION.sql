DROP TABLE D2L_USER_ATTRIBUTE_DEFINITION;
CREATE TABLE D2L_USER_ATTRIBUTE_DEFINITION (
  AttributeId NVARCHAR2(160) NOT NULL,
  Name NVARCHAR2(160) DEFAULT NULL,
  Type NVARCHAR2(10) DEFAULT NULL,
  AllowMultiple NUMBER(1) DEFAULT NULL,
  IsDefault NUMBER(1) DEFAULT NULL,
  LastModified TIMESTAMP WITH LOCAL TIME ZONE DEFAULT NULL,
  ModifiedBy NUMBER(10) DEFAULT NULL,
  IsDeleted NUMBER(1) DEFAULT NULL,
  Version NUMBER(20) DEFAULT NULL
);
CREATE UNIQUE INDEX D2L_USER_ATTRIBUTE_DEFINITION_PK ON D2L_USER_ATTRIBUTE_DEFINITION (AttributeId);
QUIT;
