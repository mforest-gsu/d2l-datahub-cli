DROP TABLE D2L_COMPETENCY_ACTIVITY_RESULT;
CREATE TABLE D2L_COMPETENCY_ACTIVITY_RESULT (
  ActivityId NUMBER(20) NOT NULL,
  OrgUnitId NUMBER(10) NOT NULL,
  UserId NUMBER(10) NOT NULL,
  LearningObjectId NUMBER(20) NOT NULL,
  IsAchieved NUMBER(1) DEFAULT NULL,
  PercentAchieved DECIMAL(19, 9) DEFAULT NULL,
  RubricScore DECIMAL(19, 9) DEFAULT NULL,
  RubricLevelAchieved NVARCHAR2(512) DEFAULT NULL,
  RubricId NUMBER(20) DEFAULT NULL,
  RubricCriterionId NUMBER(20) DEFAULT NULL,
  AchievedDate TIMESTAMP WITH LOCAL TIME ZONE DEFAULT NULL,
  Version NUMBER(20) DEFAULT NULL
);
